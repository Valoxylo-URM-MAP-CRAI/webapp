const normalizeEssenceLookupKey = (value) => (value == null ? '' : String(value))
        .toLowerCase()
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '')
        .replace(/[’']/g, '')
        .replace(/\s+/g, ' ')
        .trim();

const ESSENCES_VALOBOIS_BY_COMMON = new Map();
const ESSENCES_VALOBOIS_BY_SCIENTIFIC = new Map();
ESSENCES_VALOBOIS.forEach((essence) => {
        const commonKey = normalizeEssenceLookupKey(essence.nomUsuel);
        const scientificKey = normalizeEssenceLookupKey(essence.nomScientifique);
        if (commonKey && !ESSENCES_VALOBOIS_BY_COMMON.has(commonKey)) {
                ESSENCES_VALOBOIS_BY_COMMON.set(commonKey, essence);
        }
        if (scientificKey && !ESSENCES_VALOBOIS_BY_SCIENTIFIC.has(scientificKey)) {
                ESSENCES_VALOBOIS_BY_SCIENTIFIC.set(scientificKey, essence);
        }
});
